from typing import NamedTuple, Optional

# Portuguese/English

# Classe "Measurement" para armazenar os dados do arquivo txt
# "Measurement" class for storage txt data files


class Measurement(NamedTuple):
    time: float
    load: float
    displacement: float

# Converte uma linha do txt em um objeto "Measurement" (retorna None se for inválida)
# Conversion of a data line from txt file for "Measurement" object


def parse_line(line: str) -> Optional[Measurement]:
    parts = line.strip().replace(",", ".").split(
        "\t")  # vírgula -> ponto, separa por tab/ # comma -> point and TAB separete
    if len(parts) == 3:
        try:
            return Measurement(float(parts[0]), float(parts[1]), float(parts[2]))
        except ValueError:
            return None
    return None

# Filtro 1: filtra os dados apenas para o intervalo de deslocamento de -3.495 mm a -3.5 mm
# Filter 1: Filters the data only for the displacement range of -3.495 mm to -3.5 mm.


def displacement_filter(m: Measurement) -> bool:
    return -3.5 <= m.displacement <= -3.495

# Filtro 2: filtra os dados com diferença de tempo > 2.1 (considerando um ciclo),
# seleciona apenas a primeira Measurement de cada bloco onde o tempo difere > 2.1 s do anterior selecionado.

# Filter 2: filters data with a time difference > 2.1 (considering one cycle),
# selects only the first measurement from each block where the time differs > 2.1 s from the previously selected one.


def time_filter(measures):
    result = []
    last = None
    for m in measures:
        if last is None or m.time - last.time > 2.1:
            result.append(m)
            last = m
    return result

# Programa principal
# Main program


def main():
    # --------------------------Entrada/Input----------------------------------------------------------------------

    # recebe como entrada o arquivo txt de dados brutos gerado pelo software VectorPro após o ensaio cíclico
    # It receives as input the raw data text file generated by the VectorPro software after the cyclic test.
    with open("D:\Github\IJAMT_Availability_of_Data_and_Materials\Open-source_slicing_program_based_on_filament_extrusion_model_localized_controlled_flexibility_and_evaluation_of_cyclic_loading\Cycling_tests\_60perc\_4gap\input\_5.txt", "r", encoding="utf-8") as f:
        lines = f.read().strip().splitlines()

# --------------------------Processamento/Process----------------------------------------------------------------------

    # descarta o cabeçalho do arquivo txt de dados do ensaio cíclico
    # Discard the header of the cyclic assay data txt file
    no_header_lines = lines[1:]
    measures = filter(None, map(parse_line, no_header_lines))

    # aplica filtro 1
    # applies filter 1
    filtered_measurements = list(filter(displacement_filter, measures))

    # aplica filtro 2
    # applies filter 2
    final_measures = time_filter(filtered_measurements)

# --------------------------Saída/Output----------------------------------------------------------------------

    # gera como saída um novo arquivo txt com os dados já tratados, sendo a 1º coluna o nº do ciclo e
    # a 2ª coluna a carga no respectivo ciclo

    # Generates a new text file as output with the processed data, with the first column being the cycle number and
    # the second column being the load in the respective cycle
    with open("D:\Github\IJAMT_Availability_of_Data_and_Materials\Open-source_slicing_program_based_on_filament_extrusion_model_localized_controlled_flexibility_and_evaluation_of_cyclic_loading\Cycling_tests\_60perc\_4gap\output\_5.txt", "w", encoding="utf-8") as f_out:

        for m in final_measures:
            cycles = m.time / 2.1
            tenComp = m.load / 50
            line = f"{cycles:.3f}\t{abs(m.load):.3f}\n"
           # DEBUG linha = f"{ciclos:.3f}\t{abs(tenComp):.3f}\n"
            # troca pontos por vírgulas (opcional)
            # replace periods with commas (optional)
            line = line.replace(".", ",")
            f_out.write(line)


if __name__ == "__main__":
    main()
